x <- 5
y <- 3
# Equal to
isEqual <- x == y
print(isEqual)
# Not equal to
isNotEqual <- x != y
print(isNotEqual)
# Greater than
isGreaterThan <- x > y
print(isGreaterThan)
# Less than
isLessThan <- x < y
print(isLessThan)
# Greater than or equal to
isGreaterThanOrEqual <- x >= y
print(isGreaterThanOrEqual)
# Less than or equal to
isLessThanOrEqual <- x <= y
print(isLessThanOrEqual)
