# Create two numeric vectors
vec1 <- c(2, 4, 6, 8)
vec2 <- c(3, 5, 7, 9)
# Addition
addition <- vec1 + vec2
print(addition)
# Subtraction
subtraction <- vec1 - vec2
print(subtraction)
# Multiplication
multiplication <- vec1 * vec2
print(multiplication)
# Division
division <- vec1 / vec2
print(division)
# Exponentiation
exponentiation <- vec1 ^ vec2
print(exponentiation)
# Modulo
modulo <- vec1 %% vec2
print(modulo)
